/**
 * add - add two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the sum of two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the sub of two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the mul of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the div of two integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two integers
 * @a: the first integer
 * @b: the second integer
 * Return: the mod of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
